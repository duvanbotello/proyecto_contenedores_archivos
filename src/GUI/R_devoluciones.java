    private void cargartabla_devolciones() {
        modelo_devoluciones.setRowCount(0);
        Iterator<Abstracciones.Abs_prestamo> buscar2 = Metodos.Metodos.lista_prestamo.iterator();
        Object file[] = new Object[tb_devoluciones.getColumnCount()];
        for (Abstracciones.Abs_prestamo d : Metodos.Metodos.lista_prestamo) {
            d = buscar2.next();
            String fecha = new SimpleDateFormat("dd/MM/yyyy").format(d.getFecha_prestamo());
            String fecha1 = new SimpleDateFormat("dd/MM/yyyy").format(d.getFecha_entrega());
            file[0] = d.getId_prestamo();
            file[1] = d.getId_usuario();
            file[2] = d.getId_material();
            file[3] = fecha;
            file[4] = fecha1;
            file[5] = d.getEstado();
            modelo_devoluciones.addRow(file);
        }
    }

