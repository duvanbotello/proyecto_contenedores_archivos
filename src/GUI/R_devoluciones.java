    private void btm_validarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_validarActionPerformed
        if (fecha_Actual.getDate() != null) {
            Date fechaInicial = null;
            Date fechaFinal = fecha_Actual.getDate();
            String deuda;
            //CONVIERTO DE STRING A DATE
            try {
                fechaInicial = new SimpleDateFormat("dd/MM/yyyy").parse(txt_fecha_entrega.getText());

            } catch (ParseException ex) {
                Logger.getLogger(R_devoluciones.class.getName()).log(Level.SEVERE, null, ex);
            }
            //CALCULO LA DIFERENCIA DE LAS FECHAS DE ENTREGA
            int dias_diferencia = Metodos.Metodos.diferencia_dias_fecha(fechaFinal, fechaInicial);
            //SI LA DIFERENCIA ES = O MAYOR A UNO, COBRO LA MULTA
            if (dias_diferencia >= 1) {
                int multa = dias_diferencia * 1000;
                String botones[] = {"Pagar", "No pagar"};
                int eleccion = JOptionPane.showOptionDialog(this, "USTED DEBE CANCELAR UNA MULTA DE $" + multa + " Pesos"
                        + "\n DIAS DE RETARTODO DE ENTREGA: " + dias_diferencia,
                        "Advertencia", 0, 0, null, botones, this);
                //SI EL USUARIO DECIDE PAGAR CONFIRMO DEVOLUCION
                if (eleccion == JOptionPane.YES_OPTION) {

                    long id_prestamo = Long.parseLong(txt_idprestamo.getText());
                    //SI EL PRESTAMO YA FUE DEVUELTO, ME VOTA UN ERRO. DE LO CONTRARIO
                    //CONTINUA CON LA DEVOLUCION
                    if (comprobar_entrega(id_prestamo) == false) {
                        long id_devoluciones = Metodos.Metodos.lista_devoluciones.size() + 1;
                        long id_usuario = Long.parseLong(txt_usuario.getText());
                        long id_material = Long.parseLong(txt_idmaterial.getText());
                        Date fecha_devolucion = fecha_Actual.getDate();
                        //AGREGO EL REGISTRO DE LA DEVOLUCION
                        Metodos.Metodos.InsertarDevolucion(new Abs_devoluciones(id_devoluciones, id_usuario, id_material, fecha_devolucion));
                        Metodos.Metodos.agregar_devolucion_lista(new Abs_devoluciones(id_devoluciones, id_usuario, id_material, fecha_devolucion));

                        //ACTUALIZO LA CANTIDAD DISPONIBLE EN LA LISTA DE MATERIALES
                        aumentar_cantidad_material(id_material);
                        //GUARDO LAS MODIFICACIONES EN LOS ARCHIVOS
                        Metodos.Metodos.guarda_modificacion_materiales();
                        Metodos.Metodos.guarda_modificacion_prestamos();
                        cargartabla_devolciones();
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "POR FAVOR CANCELAR LOS ANTES POSIBLE \n ENTRE MAS DIAS SU DEUDA CRECE");
                }

            } else if (dias_diferencia < 1) {
                //SI LOS DIAS DE DIFERENCIA SON MENOR A UNO NO SE COBRA MULTA
                long id_prestamo = Long.parseLong(txt_idprestamo.getText());
                    //SI EL PRESTAMO YA FUE DEVUELTO, ME VOTA UN ERRO. DE LO CONTRARIO
                    //CONTINUA CON LA DEVOLUCION
                    if (comprobar_entrega(id_prestamo) == false) {
                        long id_devoluciones = Metodos.Metodos.lista_devoluciones.size() + 1;
                        long id_usuario = Long.parseLong(txt_usuario.getText());
                        long id_material = Long.parseLong(txt_idmaterial.getText());
                        Date fecha_devolucion = fecha_Actual.getDate();
                        //AGREGO EL REGISTRO DE LA DEVOLUCION
                        Metodos.Metodos.InsertarDevolucion(new Abs_devoluciones(id_devoluciones, id_usuario, id_material, fecha_devolucion));
                        Metodos.Metodos.agregar_devolucion_lista(new Abs_devoluciones(id_devoluciones, id_usuario, id_material, fecha_devolucion));

                        //ACTUALIZO LA CANTIDAD DISPONIBLE EN LA LISTA DE MATERIALES
                        aumentar_cantidad_material(id_material);
                        //GUARDO LAS MODIFICACIONES EN LOS ARCHIVOS
                        Metodos.Metodos.guarda_modificacion_materiales();
                        Metodos.Metodos.guarda_modificacion_prestamos();
                        cargartabla_devolciones();
                         JOptionPane.showMessageDialog(null, "GRACIAS POR ENTREGAR EL LIBRO A TIEMPO");
                    }
                   

            }

        } else {
            JOptionPane.showMessageDialog(null, "INGRESE FECHA DE ACTUAL");
        }
    }//GEN-LAST:event_btm_validarActionPerformed
    private void aumentar_cantidad_material(long id_m) {
        long id_prestamo = Long.parseLong(txt_idprestamo.getText());
        Iterator<Abs_material> buscar = Metodos.Metodos.lista_materiales.iterator();
        for (Abs_material n : Metodos.Metodos.lista_materiales) {
            n = buscar.next();
            if (id_m == n.getId_material()) {
                n.setCantidad(n.getCantidad() + 1);
                break;
            }
        }
        for (Abstracciones.Abs_prestamo d : Metodos.Metodos.lista_prestamo) {
            if (id_prestamo == d.getId_prestamo()) {
                d.setEstado("ENTREGADO");
            }
        }
    }

    private boolean comprobar_entrega(long id_prestamo) {
        boolean ya_entregado = false;
        for (Abstracciones.Abs_prestamo d : Metodos.Metodos.lista_prestamo) {
            if (id_prestamo == d.getId_prestamo()) {
                if (d.getEstado().equals("ENTREGADO")) {
                    ya_entregado = true;
                    JOptionPane.showMessageDialog(null, "EL PRESTAMO: " + d.getId_prestamo() + "\n Ya fue entregado");
                    break;
                }
            }
        }
        return ya_entregado;
    }

