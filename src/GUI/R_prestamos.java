    private void tipo_de_busqueda_ma() {
        if (cbmbuscarmateriales.getSelectedIndex() == 1) {
            tipo_busqueda = 0;
        }
        if (cbmbuscarmateriales.getSelectedIndex() == 2) {
            tipo_busqueda = 1;
        }
        if (cbmbuscarmateriales.getSelectedIndex() == 3) {
            tipo_busqueda = 2;
        }
        if (cbmbuscarmateriales.getSelectedIndex() == 4) {
            tipo_busqueda = 3;
        }
        if (cbmbuscarmateriales.getSelectedIndex() == 5) {
            tipo_busqueda = 4;
        }
    }

    private void tipo_de_busqueda_usu() {
        if (cbm_buscarusuario.getSelectedIndex() == 1) {
            tipo_busqueda = 0;
        }
        if (cbm_buscarusuario.getSelectedIndex() == 2) {
            tipo_busqueda = 1;
        }
    }
    private void txtbuscar_materialKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscar_materialKeyTyped

        if (cbmbuscarmateriales.getSelectedIndex() != 0) {

            tipo_de_busqueda_ma();
            txtbuscar_material.addKeyListener(new KeyAdapter() {
                @Override
                public void keyReleased(KeyEvent e) {
                    trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtbuscar_material.getText(), tipo_busqueda));
                }

            });
            trs = new TableRowSorter<>(tbmateriales.getModel());
            tbmateriales.setRowSorter(trs);

        } else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un tipo de busqueda");
        }

    }//GEN-LAST:event_txtbuscar_materialKeyTyped

