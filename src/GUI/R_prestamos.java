    private void cargar_tabla_materiales() {
        modelomaeriales.setRowCount(0);
        Iterator<Abstracciones.Abs_material> buscar1 = Metodos.Metodos.lista_materiales.iterator();
        Object file[] = new Object[tbmateriales.getColumnCount()];
        for (Abs_material f : Metodos.Metodos.lista_materiales) {
            String tipo1 = "";
            f = buscar1.next();
            file[0] = f.getId_material();
            file[1] = f.getNombre();
            if (f.getTipo() == 1) {
                tipo1 = "Libro";
            }
            if (f.getTipo() == 2) {
                tipo1 = "Revista";
            }
            if (f.getTipo() == 3) {
                tipo1 = "Tesis";
            }
            file[2] = tipo1;
            file[3] = f.getEditorial();
            file[4] = f.getCantidad();
            modelomaeriales.addRow(file);
        }
    }

    private void cargar_tabla_usuarios() {
        modelousuarios.setRowCount(0);
        Iterator<Abstracciones.Abs_usuario> buscar2 = Metodos.Metodos.Lista_usuarios.iterator();
        Object file[] = new Object[tbusuarios.getColumnCount()];
        for (Abstracciones.Abs_usuario d : Metodos.Metodos.Lista_usuarios) {

            d = buscar2.next();
            String tipo1 = "";
            if (d.getRol() == 1) {
                tipo1 = "Estudiante";
            }
            if (d.getRol() == 2) {
                tipo1 = "Profesor";
            }
            if (d.getRol() == 3) {
                tipo1 = "externo";
            }
            file[0] = d.getId_usuario();
            file[1] = d.getNombre();
            file[2] = tipo1;
            file[3] = d.getTelefono();
            modelousuarios.addRow(file);
        }
    }

