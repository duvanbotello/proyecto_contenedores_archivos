    private void btm_nuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_nuevoActionPerformed
        //QUE NO ESTE LA FECHA VACIA
        if (fecha.getDate() != null) {
            if (!id_usuario.getText().isEmpty()) {
                if (!p_id_libro.getText().isEmpty()) {
                    long id_prestamo = Metodos.Metodos.lista_prestamo.size() + 1;
                    String fecha1 = new SimpleDateFormat("dd/MM/yyyy").format(fecha.getDate());
                    String botones[] = {"Prestar", "Cancelar"};
                    Date fecha_Entrega = dias_de_prestamo_segun_material();
                    String fecha2 = new SimpleDateFormat("dd/MM/yyyy").format(fecha_Entrega);
                    int eleccion = JOptionPane.showOptionDialog(this, "¿Seguro que deseas prestar?" + "\n ID_PRESTAMO: " + id_prestamo + "\n ID USUARIO: " + du_documento.getText()
                            + "\n ID DE MATERIAL: " + dm_id.getText() + "\nFECHA DE PRESTAMO: " + fecha1 + "\n FECHA DE ENTREGA: " + fecha2,
                            "Advertencia", 0, 0, null, botones, this);
                    if (eleccion == JOptionPane.YES_OPTION) {
                        //EL LIBRO TIENE QUE TENER UNIDADES DISPONIBLES
                        if (Integer.parseInt(dm_cantidad.getText()) >= 1) {
                            long id_usu = Long.parseLong(du_documento.getText());
                            long id_material = Long.parseLong(dm_id.getText());
                            //AGREGO EL PRESTAMO AL CONTENEDOR
                            Metodos.Metodos.agregar_prestamo_lista(new Abs_prestamo(id_prestamo, id_usu, id_material, fecha.getDate(), fecha_Entrega, "POR ENTREGAR"));
                            Metodos.Metodos.InsertarPrestamo(new Abs_prestamo(id_prestamo, id_usu, id_material, fecha.getDate(), fecha_Entrega, "POR ENTREGAR"));
                            long id_m = Long.parseLong(dm_id.getText());
                            int cant = Integer.parseInt(dm_cantidad.getText());
                            //MODIFICO EL VALOR DE CANTIDAD DEL LIBRO
                            Iterator<Abs_material> buscar = Metodos.Metodos.lista_materiales.iterator();
                            for (Abs_material n : Metodos.Metodos.lista_materiales) {
                                n = buscar.next();
                                if (id_m == n.getId_material()) {
                                    n.setCantidad(cant - 1);
                                    break;
                                }
                            }
                            Metodos.Metodos.guarda_modificacion_materiales();
                 
                            cargar_tabla_materiales();
                            limpiar_campos();

                        } else {
                            JOptionPane.showMessageDialog(null, "No hay unidades disponibles");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "SELECCIONE UN MATERIAL");
                }
            } else {
                JOptionPane.showMessageDialog(null, "SELECCIONE UN USUARIO");
            }
        } else {
            JOptionPane.showMessageDialog(null, "INGRESE UNA FECHA DE PRESTAMO");
        }

    }//GEN-LAST:event_btm_nuevoActionPerformed
    private Date dias_de_prestamo_segun_material() {
        Date fechaentrega = null;
//        e.setText(fecha2);
        if (dm_tipo.getText().equals("Libro")) {
            fechaentrega = Metodos.Metodos.sumarDiasAFecha(fecha.getDate(), 3);
        }
        if (dm_tipo.getText().equals("Revista")) {
            fechaentrega = Metodos.Metodos.sumarDiasAFecha(fecha.getDate(), 4);
        }
        if (dm_tipo.getText().equals("Tesis")) {
            fechaentrega = Metodos.Metodos.sumarDiasAFecha(fecha.getDate(), 5);
        }
        return fechaentrega;
    }

